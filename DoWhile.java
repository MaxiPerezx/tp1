package tp1;

public class DoWhile {

	public static void main(String[] args) {
		
		 int i = 1;
	        System.out.println("Numeros del 1 al 100: (Usando Do While) ");                                                             
	        do{
	             System.out.println(i);
	                   i++;
	       
	        }while(i<=100); 
	        
	}
}
